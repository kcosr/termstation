#!/usr/bin/env node

/**
 * gen-build-info.mjs
 *
 * Generates build information from VERSION file and git metadata.
 * Outputs shared/build-info.generated.mjs for use by backend, frontend, and desktop.
 *
 * Build number resolution order:
 * 1. TS_BUILD_NUMBER or BUILD_NUMBER env var (CI override)
 * 2. git rev-list --count HEAD (if .git exists)
 * 3. Default to 0 with warning
 *
 * Usage:
 *   node scripts/gen-build-info.mjs           # Generate build info
 *   node scripts/gen-build-info.mjs --check   # Verify VERSION matches git tag (for releases)
 */

import fs from 'fs';
import path from 'path';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.join(__dirname, '..');

const versionFilePath = path.join(rootDir, 'VERSION');
const outputPath = path.join(rootDir, 'shared', 'build-info.generated.mjs');
const outputPathCjs = path.join(rootDir, 'shared', 'build-info.generated.cjs');

/**
 * Read VERSION file
 */
function readVersion() {
  try {
    const version = fs.readFileSync(versionFilePath, 'utf8').trim();
    if (!version) {
      throw new Error('VERSION file is empty');
    }
    // Validate semver format (loose check)
    if (!/^\d+\.\d+\.\d+(-[\w.]+)?$/.test(version)) {
      console.warn(`Warning: VERSION "${version}" doesn't look like semver (X.Y.Z)`);
    }
    return version;
  } catch (e) {
    console.error(`Error reading VERSION file: ${e.message}`);
    return '0.0.0';
  }
}

/**
 * Get build number from env var or git
 */
function getBuildNumber() {
  // 1. Check env vars (CI override)
  const envBuild = process.env.TS_BUILD_NUMBER || process.env.BUILD_NUMBER;
  if (envBuild) {
    const num = parseInt(envBuild, 10);
    if (!isNaN(num) && num >= 0) {
      console.log(`Build number from env: ${num}`);
      return num;
    }
  }

  // 2. Try git commit count
  try {
    const gitDir = path.join(rootDir, '.git');
    if (fs.existsSync(gitDir)) {
      const count = execSync('git rev-list --count HEAD', {
        cwd: rootDir,
        encoding: 'utf8',
        stdio: ['pipe', 'pipe', 'pipe']
      }).trim();
      const num = parseInt(count, 10);
      if (!isNaN(num)) {
        console.log(`Build number from git: ${num}`);
        return num;
      }
    }
  } catch (e) {
    console.warn(`Warning: Could not get git commit count: ${e.message}`);
  }

  // 3. Default with warning
  console.warn('Warning: Could not determine build number, defaulting to 0');
  return 0;
}

/**
 * Get git commit SHA
 */
function getCommitSha() {
  try {
    const gitDir = path.join(rootDir, '.git');
    if (fs.existsSync(gitDir)) {
      const sha = execSync('git rev-parse --short HEAD', {
        cwd: rootDir,
        encoding: 'utf8',
        stdio: ['pipe', 'pipe', 'pipe']
      }).trim();
      return sha;
    }
  } catch (e) {
    // Ignore
  }
  return null;
}

/**
 * Check if VERSION matches current git tag (for release verification)
 */
function checkTagMatch(version) {
  try {
    const tag = execSync('git describe --tags --exact-match HEAD 2>/dev/null || echo ""', {
      cwd: rootDir,
      encoding: 'utf8',
      stdio: ['pipe', 'pipe', 'pipe']
    }).trim();

    if (!tag) {
      console.log('No git tag on current commit (not a tagged release)');
      return true;
    }

    const expectedTag = `v${version}`;
    if (tag === expectedTag) {
      console.log(`Tag matches VERSION: ${tag}`);
      return true;
    } else {
      console.error(`Tag mismatch: git tag is "${tag}" but VERSION is "${version}" (expected tag "${expectedTag}")`);
      return false;
    }
  } catch (e) {
    console.warn(`Warning: Could not check git tag: ${e.message}`);
    return true;
  }
}

/**
 * Generate build info module
 */
function generateBuildInfo() {
  const version = readVersion();
  const build = getBuildNumber();
  const commit = getCommitSha();
  const timestamp = new Date().toISOString();

  // Check for --check flag
  if (process.argv.includes('--check')) {
    const matches = checkTagMatch(version);
    process.exit(matches ? 0 : 1);
  }

  const buildInfo = { version, build, commit, timestamp };
  console.log(`Generating build info: v${version} (build ${build}${commit ? `, ${commit}` : ''})`);

  // Generate ESM module
  const esmContent = `// AUTO-GENERATED FILE - DO NOT EDIT
// Generated by scripts/gen-build-info.mjs at ${timestamp}

export const version = '${version}';
export const build = ${build};
export const commit = ${commit ? `'${commit}'` : 'null'};
export const timestamp = '${timestamp}';
export const versionFull = '${version}+${build}';

export default { version, build, commit, timestamp, versionFull };
`;

  // Generate CommonJS module (for shared/version.js compatibility)
  const cjsContent = `// AUTO-GENERATED FILE - DO NOT EDIT
// Generated by scripts/gen-build-info.mjs at ${timestamp}

const version = '${version}';
const build = ${build};
const commit = ${commit ? `'${commit}'` : 'null'};
const timestamp = '${timestamp}';
const versionFull = '${version}+${build}';

module.exports = { version, build, commit, timestamp, versionFull };
`;

  // Write files
  fs.writeFileSync(outputPath, esmContent, 'utf8');
  console.log(`Written: ${path.relative(rootDir, outputPath)}`);

  fs.writeFileSync(outputPathCjs, cjsContent, 'utf8');
  console.log(`Written: ${path.relative(rootDir, outputPathCjs)}`);

  return buildInfo;
}

// Run
generateBuildInfo();
